var = '27' #as string

#%i -  Signed Decimal Integer
print('Variable as signed decimal integer = %i' %(int(var)))

#%o - Octal Integer
print('Variable as octal integer = %o' %(int(var)))

#%x - Hexa Decimal Integer
print('Variable as Hexa Decimal Integer = %X' %(int(var)))

#%f - Float
print('Variable as Floating Point Real Num = %f' %(float(var)))

#%x - Exonential Notation
print('Variable as Exonential Notation = %e' %(int(var)))

